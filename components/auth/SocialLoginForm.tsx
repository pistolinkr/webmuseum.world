'use client';

import { useState } from 'react';
import { useAuth } from '@/contexts/AuthContext';

interface SocialLoginFormProps {
  onSuccess?: () => void;
  onSwitchToSignUp?: () => void;
}

export default function SocialLoginForm({ onSuccess, onSwitchToSignUp }: SocialLoginFormProps) {
  const { signInWithGoogle, signInWithMicrosoft, signInWithApple, signInWithGitHub, sendMagicLink, signInWithMagicLink, isMagicLink } = useAuth();
  const [error, setError] = useState<Record<string, boolean>>({});
  const [loading, setLoading] = useState<'google' | 'microsoft' | 'apple' | 'github' | 'email' | null>(null);
  const [email, setEmail] = useState('');
  const [emailSent, setEmailSent] = useState(false);

  const handleGoogleSignIn = async () => {
    setError((prev) => ({ ...prev, google: false }));
    setLoading('google');
    
    try {
      await signInWithGoogle();
      onSuccess?.();
    } catch (err: any) {
      setError((prev) => ({ ...prev, google: true }));
      setTimeout(() => {
        setError((prev) => ({ ...prev, google: false }));
      }, 5000);
    } finally {
      setLoading(null);
    }
  };

  const handleMicrosoftSignIn = async () => {
    setError((prev) => ({ ...prev, microsoft: false }));
    setLoading('microsoft');
    
    try {
      await signInWithMicrosoft();
      onSuccess?.();
    } catch (err: any) {
      setError((prev) => ({ ...prev, microsoft: true }));
      setTimeout(() => {
        setError((prev) => ({ ...prev, microsoft: false }));
      }, 5000);
    } finally {
      setLoading(null);
    }
  };

  const handleAppleSignIn = async () => {
    setError((prev) => ({ ...prev, apple: false }));
    setLoading('apple');
    
    try {
      await signInWithApple();
      onSuccess?.();
    } catch (err: any) {
      setError((prev) => ({ ...prev, apple: true }));
      setTimeout(() => {
        setError((prev) => ({ ...prev, apple: false }));
      }, 5000);
    } finally {
      setLoading(null);
    }
  };

  const handleGitHubSignIn = async () => {
    setError((prev) => ({ ...prev, github: false }));
    setLoading('github');
    
    try {
      await signInWithGitHub();
      onSuccess?.();
    } catch (err: any) {
      setError((prev) => ({ ...prev, github: true }));
      setTimeout(() => {
        setError((prev) => ({ ...prev, github: false }));
      }, 5000);
    } finally {
      setLoading(null);
    }
  };

  const handleEmailSignIn = async (e: React.FormEvent) => {
    e.preventDefault();
    setError((prev) => ({ ...prev, email: false }));
    setLoading('email');
    
    try {
      await sendMagicLink(email);
      setEmailSent(true);
    } catch (err: any) {
      setError((prev) => ({ ...prev, email: true }));
      setTimeout(() => {
        setError((prev) => ({ ...prev, email: false }));
      }, 5000);
    } finally {
      setLoading(null);
    }
  };

  if (emailSent) {
    return (
      <div className="auth-form">
        <div className="auth-form__success">
          <p><strong>Check your email!</strong></p>
          <p>We've sent you a sign-in link. Click the link in the email to sign in.</p>
          <p className="auth-form__success-hint">
            The link will expire in 1 hour. Make sure to check your spam folder if you don't see it.
          </p>
          <button
            type="button"
            onClick={() => {
              setEmailSent(false);
              setEmail('');
            }}
            className="auth-form__button auth-form__button--secondary"
          >
            Send another link
          </button>
        </div>
      </div>
    );
  }

  return (
    <div className="auth-form">
      <div className="auth-form__social-buttons">
        <button
          type="button"
          onClick={handleMicrosoftSignIn}
          disabled={!!loading}
          className={`auth-form__social-button ${error.microsoft ? 'auth-form__social-button--error' : ''}`}
        >
          <svg className="auth-form__social-icon" viewBox="0 0 24 24" width="20" height="20">
            <path fill="#F25022" d="M1 1h10v10H1z"/>
            <path fill="#00A4EF" d="M13 1h10v10H13z"/>
            <path fill="#7FBA00" d="M1 13h10v10H1z"/>
            <path fill="#FFB900" d="M13 13h10v10H13z"/>
          </svg>
          {error.microsoft 
            ? 'Authentication Failed, please try again.' 
            : loading === 'microsoft' 
            ? 'Signing in...' 
            : 'Continue with Microsoft'}
        </button>

        <button
          type="button"
          onClick={handleGoogleSignIn}
          disabled={!!loading}
          className={`auth-form__social-button ${error.google ? 'auth-form__social-button--error' : ''}`}
        >
          <svg className="auth-form__social-icon" viewBox="0 0 24 24" width="20" height="20">
            <path fill="#4285F4" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"/>
            <path fill="#34A853" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"/>
            <path fill="#FBBC05" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"/>
            <path fill="#EA4335" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"/>
          </svg>
          {error.google 
            ? 'Authentication Failed, please try again.' 
            : loading === 'google' 
            ? 'Signing in...' 
            : 'Continue with Google'}
        </button>

        <button
          type="button"
          onClick={handleGitHubSignIn}
          disabled={!!loading}
          className={`auth-form__social-button ${error.github ? 'auth-form__social-button--error' : ''}`}
        >
          <svg className="auth-form__social-icon" viewBox="0 0 24 24" width="20" height="20" fill="currentColor">
            <path d="M12 0c-6.626 0-12 5.373-12 12 0 5.302 3.438 9.8 8.207 11.387.599.111.793-.261.793-.577v-2.234c-3.338.726-4.033-1.416-4.033-1.416-.546-1.387-1.333-1.756-1.333-1.756-1.089-.745.083-.729.083-.729 1.205.084 1.839 1.237 1.839 1.237 1.07 1.834 2.807 1.304 3.492.997.107-.775.418-1.305.762-1.604-2.665-.305-5.467-1.334-5.467-5.931 0-1.311.469-2.381 1.236-3.221-.124-.303-.535-1.524.117-3.176 0 0 1.008-.322 3.301 1.23.957-.266 1.983-.399 3.003-.404 1.02.005 2.047.138 3.006.404 2.291-1.552 3.297-1.23 3.297-1.23.653 1.653.242 2.874.118 3.176.77.84 1.235 1.911 1.235 3.221 0 4.609-2.807 5.624-5.479 5.921.43.372.823 1.102.823 2.222v3.293c0 .319.192.694.801.576 4.765-1.589 8.199-6.086 8.199-11.386 0-6.627-5.373-12-12-12z"/>
          </svg>
          {error.github 
            ? 'Authentication Failed, please try again.' 
            : loading === 'github' 
            ? 'Signing in...' 
            : 'Continue with GitHub'}
        </button>

        <button
          type="button"
          onClick={handleAppleSignIn}
          disabled={!!loading}
          className={`auth-form__social-button ${error.apple ? 'auth-form__social-button--error' : ''}`}
        >
          <svg className="auth-form__social-icon" viewBox="0 0 24 24" width="20" height="20" fill="currentColor">
            <path d="M17.05 20.28c-.98.95-2.05.88-3.08.4-1.09-.5-2.08-.48-3.24 0-1.44.62-2.2.44-3.06-.4C2.79 15.25 3.51 7.59 9.05 7.31c1.35.07 2.29.74 3.08.8 1.18-.24 2.31-.93 3.57-.84 1.51.12 2.65.72 3.4 1.8-3.12 1.87-2.38 5.98.48 7.13-.57 1.5-1.31 2.99-2.54 4.09l.01-.01zM12.03 7.25c-.15-2.23 1.66-4.07 3.74-4.25.29 2.58-2.34 4.5-3.74 4.25z"/>
          </svg>
          {error.apple 
            ? 'Authentication Failed, please try again.' 
            : loading === 'apple' 
            ? 'Signing in...' 
            : 'Continue with Apple'}
        </button>

        <form onSubmit={handleEmailSignIn} className="auth-form__email-form">
          <div className="auth-form__field">
            <input
              type="email"
              value={email}
              onChange={(e) => setEmail(e.target.value)}
              required
              className="auth-form__input"
              placeholder="your@email.com"
              disabled={!!loading}
            />
          </div>
          <button
            type="submit"
            disabled={!!loading}
            className={`auth-form__social-button ${error.email ? 'auth-form__social-button--error' : ''}`}
          >
            <svg className="auth-form__social-icon" viewBox="0 0 24 24" width="20" height="20" fill="none" stroke="currentColor" strokeWidth="2">
              <path d="M4 4h16c1.1 0 2 .9 2 2v12c0 1.1-.9 2-2 2H4c-1.1 0-2-.9-2-2V6c0-1.1.9-2 2-2z"/>
              <polyline points="22,6 12,13 2,6"/>
            </svg>
            {error.email 
              ? 'Authentication Failed, please try again.' 
              : loading === 'email' 
              ? 'Sending...' 
              : 'Continue with Email'}
          </button>
        </form>
      </div>
    </div>
  );
}

